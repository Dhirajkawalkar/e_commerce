/// Defines the different types of SnackBars available in the application.
/// 
/// Each type typically corresponds to a different color or icon to provide
/// visual feedback to the user about the nature of the message.
enum SnackBarType {
  /// Indicates a successful operation.
  success,
  
  /// Indicates a warning or a potential issue.
  warning,
  
  /// Indicates an error or a failed operation.
  error,
  
  /// Indicates general information.
  info,
}

